  <h2>Website Taftisan</h2>
  <ol>
    <li>
      <h3>Tujuan</h3>
      <p>Website ini di buat sesederhana mungkin dengan tujuan memudahkan dan menjaga niat dari para santri yang ingin menghafal Alfiyyah Ibn Malik, dengan beberapa fitur yang membuat mereka bisa aktif dan test hafalannya sendiri</h3>
    </li>
    <li>
      <h3>Fitur</h3>

      <ul>

        <li>
          <h4>Bait Alfiyah Lengkap :</h4>
          <p>Saat ini untuk bait yang di update baru 500 bait, dengan fitur loncat bait, dan mode awal satar untuk melatih hafalan santri</p>
        </li>
        <li>
          <h4>Mode Quiz</h4>
          <p>Mode untuk melatih melanjutkan bait dengan bait acak, cocok untuk simulasi taftisan</p>
        </li>
        <li>
          <h4>Mode Lalaran</h4>
          <p>Mode untuk melatih melanjutkan bait dengan bait berutan sesuai setting, cocok untuk hafalan baru atau yang belum kuat hafalannya</p>
        </li>
        <li>
          <h4>Mode Mumarrin</h4>
          <p>Mode untuk melatih melanjutkan bait untuk mengetes orang lain, cocok untuk gambaran taftisan dengan bantuan teman</p>
        </li>
        <li>
          <h4>Riwayat</h4>
          <p>Hasil Nilai sesuai dengan nama yang di input terakhir kali</p>
        </li>
        <li>
          <h4>Logout</h4>
          <p>Menghapus nama dan data di setting dari database (untuk yang masuk di nilai akhir maka tetap tersimpan)</p>
        </li>
      </ul>
    </li>
